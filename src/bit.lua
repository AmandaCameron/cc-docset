--- Bitmask APIs.
-- @module bit

---------
-- Preforms a left shift.
-- @function blshift
-- @int n The number to shift.
-- @int bits The number of bits to shift by.
-- @treturn int The result of the bitwise left shift.

---------
-- Preforms a right shift.
-- @function brshift
-- @int n The number to shift.
-- @int bits The number of bits to shift by.
-- @treturn int The result of the bitwise right shift.

---------
-- Preforms a logical right-shift.
-- @function blogic_rshift
-- @int n The number to shift.
-- @int bits The number of bits to shift by.
-- @treturn int The result of the logical bitwise right shift.

---------
-- bitwise XORs the given numbers.
-- @function bxor
-- @int m
-- @int n
-- @treturn int The result of the bitwise exclusive-or.

---------
-- bitwise ORs two numbers.
-- @function bor
-- @int m
-- @int n
-- @treturn int The result of the bitwise or.

---------
-- bitwise ANDs two numbers.
-- @function band
-- @int m
-- @int n
-- @treturn int The result of the bitwise and.

---------
-- Preforms a bitwise NOT of a number.
-- @function bnot
-- @int m The number to NOT.
-- @treturn int The result of the bitwise not.
